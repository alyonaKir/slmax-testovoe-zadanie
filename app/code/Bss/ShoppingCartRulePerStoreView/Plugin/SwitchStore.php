namespace Bss\ShoppingCartRulePerStoreView\Plugin;

class SwitchStore
{
    public $cart;

    public function __construct(
        \Magento\Checkout\Model\Cart $cart
    ) {
        $this->cart = $cart;
    }

    public function afterExecute()
    {
        $this->cart->updateItems([])->save();
    }
}
